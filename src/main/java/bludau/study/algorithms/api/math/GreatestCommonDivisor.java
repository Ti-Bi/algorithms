package bludau.study.algorithms.api.math;

/**
 * Calculation of the greatest common division of two integer numbers.
 * 
 * @author Anatol Bludau
 *
 */
public interface GreatestCommonDivisor {

	/**
	 * Finds the greatest common division of two integer numbers.
	 * 
	 * @param p
	 *            first number
	 * @param q
	 *            second number
	 * @return the greatest common division
	 */
	int findGCD(int p, int q);
}
